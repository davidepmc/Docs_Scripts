		column APPLIED_SCN format 999999999999999999
		column NEWEST_SCN format 999999999999999999
		column READ_SCN format 999999999999999999
		SELECT L.SEQUENCE#, to_char(L.FIRST_TIME,'dd/mm/yyyy hh24:mi:ss'), 
(CASE WHEN L.NEXT_CHANGE# < P.READ_SCN 
THEN 'YES' WHEN L.FIRST_CHANGE# < P.APPLIED_SCN 
THEN 'CURRENT'ELSE 'NO' END) APPLIED 
FROM DBA_LOGSTDBY_LOG L, DBA_LOGSTDBY_PROGRESS P
 where l.first_time >= sysdate - 3 ORDER BY SEQUENCE#;
